public class LearnString{
	public static void main(String [] args){
		
		String es = "Isuru Isuru Isuru"; // Empty String
		String name = new String();
		System.out.println("Name: "+es);
		System.out.println("Name is: "+name); //Null String
		
		//Length of the String
		System.out.println("Length of the string is: "+es.length());
		
		char ps = es.charAt(10);
		System.out.println("Character in the 10th position is: "+ps);
		
		
		
	}
}
