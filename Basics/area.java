public class area{
	
	public static void main (String [] args){
		
		//area of circle
		
		int r = 4;
		double AreaOfCircle = 3.14 * r * r;
		System.out.println("Area of the circle : "+AreaOfCircle);
		
		//area of square
		
		int l = 5;
		int AreaOfSquare = l * l;
		System.out.println("Area of the square :"+AreaOfSquare);
		
		//area of rectangle
		
		int w = 5;
		int h = 6;
		int AreaOfRectangle=w * h;
		System.out.println("Area of the rectangle :"+AreaOfRectangle);
		System.out.println("Area of the rectangle :"+AreaOfRectangle);
		
		//area of triangle
		
		int b = 10;
		int ht = 6;
		double AreaOfTriangle = 0.5 * b * ht;
		System.out.println("Area of the triangle :"+AreaOfTriangle);
		
		
		
		

		
	}
}
